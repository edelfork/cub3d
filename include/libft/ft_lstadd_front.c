#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *next)
{
	if (!next)
		return ;
	if (!*lst)
		*lst = next;
	if (lst && next)
	{
		next->next = *lst;
		*lst = next;
	}
}
